
#some_list[start:end:step]
#reverse order variable[::-1]

first_list = [1, 2, 3, 4]

first_list[1:] # [2, 3, 4]

first_list[3:] # [4]

first_list[-1:] # [4]

first_list[-3:] # [2, 3, 4]

first_list[:2] # [1, 2]

first_list[1:3] # [2, 3]

first_list[:-1] # [1, 2, 3]

first_list[1:-1] #[2, 3]

second_list = [1, 2, 3, 4, 5, 6]

second_list[1::2] #[2, 4, 6]

second_list[::2] #[1, 3, 5]

second_list[:1:-1] #[6, 5, 4, 3]

second_list[2::-1] #[3, 2, 1]
